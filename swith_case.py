# switch case 

def switch_case(s):
    """
    Switches the case of a given string.

    Parameters:
        s (str): The string to switch the case of.

    Returns:
        str: The string with the case switched.
    """
    # Swap the case of the string
    return s.swapcase()


# Example usage
input_string = "Hello, World!"
result = switch_case(input_string)
print(result)  # Output: hELLO, wORLD!  # Output: hELLO, wORLD!

