# arithmetic operations
"""
This script demonstrates various types of operations in Python, including:

1. Arithmetic operations:
    - Addition: 2 + 3
    - Subtraction: 5 - 7
    - Multiplication: 4 * 6
    - Division: 10 / 3
    - Floor Division: 10 // 3
    - Modulus: 10 % 3
    - Exponentiation: 2 ** 3
    - Order of operations: 5 + 3 * 2

2. Comparison operations:
    - Equal to: 5 == 3
    - Not equal to: 5 != 3
    - Greater than: 5 > 3
    - Less than: 5 < 3
    - Greater than or equal to: 5 >= 3
    - Less than or equal to: 5 <= 3

3. Logical operations:
    - Logical AND: True and True, True and False
    - Logical OR: False or True, False or False
    - Logical NOT: not True, not False

4. Assignment operations:
    - Basic assignment: x = 5
    - Addition assignment: x += 2
    - Subtraction assignment: x -= 3
    - Multiplication assignment: x *= 4
    - Division assignment: x /= 2
"""
print(2 + 3)
print(5 - 7)
print(4 * 6)
print(10 / 3)   # division
print(10 // 3)  # floor division
print(10 % 3)   # modulus   (remainder) 
print(2 ** 3)   # exponentiation
print(5 + 3 * 2) # order of operations

# comparison operations
print(5 == 3)   # equal to
print(5 != 3)   # not equal to
print(5 > 3)    # greater than
print(5 < 3)    # less than
print(5 >= 3)   # greater than or equal to
print(5 <= 3)   # less than or equal to

print('**********')

# logical operations
print(True and True)
print(True and False)
print(False or True)
print(False or False)
print(not True)
print(not False)

# assignment operations
x = 5
print(x)
x += 2  # x = x + 2
print(x)
x -= 3  # x = x - 3
print(x)
x *= 4  # x = x * 4
print(x)
x /= 2  # x = x / 2
print(x)    # x = 4.0

# end of operators.py

